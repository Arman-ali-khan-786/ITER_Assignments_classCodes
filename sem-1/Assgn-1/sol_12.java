public class sol_12 {

	public static void main(String[] args) {
		
	  double a=3.14159;
		System.out.println(a);               //--->  3.14159
		System.out.println(a+1);             //--->  4.14159
		System.out.println(8/(int)a);        //--->  2
		System.out.println(8/a);             //--->  2.5464812403910124
		System.out.println((int)(8/a));      //--->  2
  }
}
